/*
JavaScript is a dynamically typed language. 
This means that variable types are determined at runtime,
 and you do not need to explicitly declare the type of a 
 variable before using it. You can assign different types of
  values to a variable during its lifetime.

For example, in JavaScript, you can do the following:

let x = 10; // x is now a number
x = "Hello"; // x is now a string
x = true; // x is now a boolean
On the other hand, statically typed languages require you to declare the variable's type explicitly, and the type checking is done at compile-time, before the code is executed
 */


